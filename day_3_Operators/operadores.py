#------------- OPERADORES ARITMETICOS --------------------

# Suma
suma = 12 + 5
print(suma)

# Resta
resta = 12 - 5
print(resta)

# Multiplicacion
multi = 12*5
print(multi)

# Division
division = 12 / 5 # devuelve un float
print(division)

# Potenciacion
exponente = 12**5
print(exponente)

# Division baja
division_baja = 12 // 5 # devuelve un entero redondeado hacia abajo
print(division_baja)

# Resto o modulo
resto = 12 % 5
print(resto)

# Mostrar el tipo de dato
tipo_de_dato = type({"hola", 23, False})
print(tipo_de_dato)
# 1. escriba un programa que sume todos los elementos de una lista
lista_numbers = [1,5,7,9]
count = 0

for i in lista_numbers:
    count += i
print(count)

# 2. Escriba un programa en Python para multiplicar todos los elementos de una lista
mult = 1
for i in lista_numbers:
    mult *= i
print(mult)